package com.academy.tests.pocunittest.argumet;

public class User {

	private String email;
	private String name;

	// Construtor, getters e setters
	public User(String email, String name) {
		this.email = email;
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public String getName() {
		return name;
	}
}
